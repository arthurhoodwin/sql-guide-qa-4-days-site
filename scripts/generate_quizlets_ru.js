const cards = [];
let id = 1;
const seen = new Set();

function add(topic, front, back) {
  const key = `${topic}::${front}`;
  if (seen.has(key)) return;
  seen.add(key);
  cards.push({ id: `q${id++}`, topic, level: "Стажер/Джун", front, back });
}

