function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function normalizeSearchText(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[^\p{L}\p{N}\s-]/gu, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function tokenizeSearch(value) {
  return normalizeSearchText(value)
    .split(" ")
    .map((token) => token.trim())
    .filter((token) => token.length > 1);
}

function stemSearchToken(token) {
  const t = String(token || "");
  if (t.length <= 4) return t;

  if (/^[a-z]+$/i.test(t)) {
    return t
      .replace(/(ations|ation|ments|ment|ingly|edly|ing|ed|ly|es|s)$/i, "")
      .trim() || t;
  }

  return t
    .replace(/(иями|ями|ами|ого|его|ому|ему|ыми|ими|иях|иям|ией|ция|ции|ций|ировать|ного|ными|ный|ная|ное|ные|ать|ить|ешь|ете|ют|ет|ая|яя|ое|ее|ые|ие|ов|ев|ом|ем|ам|ям|ах|ях|а|я|ы|и|о|е|у|ю)$/u, "")
    .trim() || t;
}

function stripMarkdownSyntax(value) {
  return String(value || "")
    .replace(/`{1,3}[^`]*`{1,3}/g, " ")
    .replace(/!\[[^\]]*\]\([^\)]*\)/g, " ")
    .replace(/\[[^\]]*\]\([^\)]*\)/g, " ")
    .replace(/[*_>#~\-]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function buildSearchSynonymLookup() {
  if (searchSynonymLookup) return searchSynonymLookup;
  const lookup = new Map();
  SEARCH_SYNONYM_GROUPS.forEach((group) => {
    const normalizedGroup = [...new Set(group.map((token) => normalizeSearchText(token)).filter(Boolean))];
    normalizedGroup.forEach((token) => {
      lookup.set(token, normalizedGroup);
      const stem = stemSearchToken(token);
      if (!lookup.has(stem)) lookup.set(stem, normalizedGroup);
    });
  });
  searchSynonymLookup = lookup;
  return lookup;
}

function expandSearchTokens(tokens) {
  const lookup = buildSearchSynonymLookup();
  const expanded = new Set();
  tokens.forEach((token) => {
    const normalized = normalizeSearchText(token);
    if (!normalized) return;
    const stem = stemSearchToken(normalized);
    expanded.add(normalized);
    expanded.add(stem);
    const group = lookup.get(normalized) || lookup.get(stem);
    if (group) {
      group.forEach((item) => {
        expanded.add(item);
        expanded.add(stemSearchToken(item));
      });
    }
  });
  return [...expanded].filter(Boolean);
}

function parseMarkdownSectionsForSearch(markdownText, source) {
  const lines = String(markdownText || "").replace(/\r/g, "").split("\n");
  const sections = [];
  const usedIds = new Set();
  let sectionOrder = 0;

  const uniqueHeadingId = (text, fallback = "section") => {
    const base = slugify(text || fallback) || fallback;
    let id = base;
    let seq = 2;
    while (usedIds.has(id)) {
      id = `${base}-${seq}`;
      seq += 1;
    }
    usedIds.add(id);
    return id;
  };

  let currentH2 = "Введение";
  let currentH2Id = uniqueHeadingId(currentH2, "intro");
  let currentH3 = "";
  let currentH3Id = "";
  let buffer = [];

  const flush = () => {
    const cleaned = stripMarkdownSyntax(buffer.join(" "));
    buffer = [];
    if (!cleaned) return;

    const sectionTitle = currentH3 || currentH2 || source.title;
    const sectionPath = currentH3 ? `${currentH2} → ${currentH3}` : currentH2;
    const anchorId = currentH3Id || currentH2Id;
    sectionOrder += 1;
    sections.push({
      sourceTitle: source.title,
      href: `${source.href}${anchorId ? `#${anchorId}` : ""}`,
      sectionTitle,
      sectionPath,
      sourceOrder: Number(source.sourceOrder || 0),
      sectionOrder,
      body: cleaned,
      headingSearch: normalizeSearchText(`${sectionTitle} ${sectionPath}`)
    });
  };

  lines.forEach((line) => {
    const h2Match = line.match(/^##\s+(.+)$/);
    if (h2Match) {
      flush();
      currentH2 = stripMarkdownSyntax(h2Match[1]);
      currentH2Id = uniqueHeadingId(currentH2, "section");
      currentH3 = "";
      currentH3Id = "";
      return;
    }

    const h3Match = line.match(/^###\s+(.+)$/);
    if (h3Match) {
      flush();
      currentH3 = stripMarkdownSyntax(h3Match[1]);
      currentH3Id = uniqueHeadingId(currentH3, "subsection");
      return;
    }

    if (/^#\s+/.test(line) || /^####\s+/.test(line)) return;
    buffer.push(line);
  });

  flush();

  return sections.map((section) => {
    const searchText = normalizeSearchText(`${section.sectionTitle} ${section.sectionPath} ${section.body}`);
    const tokens = tokenizeSearch(searchText);
    const stems = tokens.map(stemSearchToken);
    const tokenFreq = new Map();
    const stemFreq = new Map();
    tokens.forEach((token) => tokenFreq.set(token, (tokenFreq.get(token) || 0) + 1));
    stems.forEach((stem) => stemFreq.set(stem, (stemFreq.get(stem) || 0) + 1));
    return {
      ...section,
      searchText,
      tokens,
      stems,
      tokenFreq,
      stemFreq,
      tokenSet: new Set(tokens),
      stemSet: new Set(stems)
    };
  });
}

async function buildLessonSearchIndex() {
  if (lessonSearchIndexPromise) return lessonSearchIndexPromise;
  lessonSearchIndexPromise = Promise.all(
    LESSON_SEARCH_SOURCES.map(async (source, sourceOrder) => {
      try {
        const response = await fetch(source.path);
        if (!response.ok) return [];
        const markdown = await response.text();
        return parseMarkdownSectionsForSearch(markdown, { ...source, sourceOrder });
      } catch {
        return [];
      }
    })
  ).then((allSections) => allSections.flat());
  return lessonSearchIndexPromise;
}

function getSectionKeywordFrequency(section, queryTokens, expandedTokens) {
  const queryUnique = [...new Set(queryTokens.map(normalizeSearchText).filter(Boolean))];
  const expandedUnique = [...new Set(expandedTokens.map(normalizeSearchText).filter(Boolean))];
  let queryHits = 0;
  let expandedHits = 0;

  queryUnique.forEach((token) => {
    const exact = section.tokenFreq.get(token) || 0;
    if (exact > 0) {
      queryHits += exact;
      return;
    }
    const stem = stemSearchToken(token);
    queryHits += section.stemFreq.get(stem) || 0;
  });

  expandedUnique.forEach((token) => {
    if (queryUnique.includes(token)) return;
    expandedHits += section.tokenFreq.get(token) || 0;
  });

  return {
    queryHits,
    expandedHits,
    total: (queryHits * 3) + expandedHits
  };
}

function scoreSearchSection(section, rawQuery, queryTokens, expandedTokens) {
  if (!expandedTokens.length) return 0;
  let score = 0;
  let hits = 0;
  const normalizedQuery = normalizeSearchText(rawQuery);
  const freq = getSectionKeywordFrequency(section, queryTokens, expandedTokens);

  if (normalizedQuery && section.searchText.includes(normalizedQuery)) score += 10;

  expandedTokens.forEach((token) => {
    const stem = stemSearchToken(token);
    if (section.tokenSet.has(token)) {
      score += 4;
      hits += 1;
      return;
    }
    if (section.stemSet.has(stem)) {
      score += 2;
      hits += 1;
      return;
    }
    if (section.searchText.includes(token)) {
      score += 1;
      hits += 1;
    }
  });

  if (freq.total > 0) score += freq.total * 12;
  if (hits === 0 && !(normalizedQuery && section.searchText.includes(normalizedQuery)) && freq.total === 0) return 0;
  if (section.headingSearch.includes(normalizedQuery)) score += 6;
  return { score, freq };
}

function extractSearchSnippet(section, queryTokens) {
  const body = section.body || "";
  if (!body) return "";
  const tokens = queryTokens.map((token) => normalizeSearchText(token)).filter(Boolean);
  const lines = body.split(/(?<=[.!?])\s+/);
  const best = lines.find((line) => {
    const normalized = normalizeSearchText(line);
    return tokens.some((token) => normalized.includes(token) || normalized.includes(stemSearchToken(token)));
  }) || lines[0];
  return best.length > 220 ? `${best.slice(0, 220)}…` : best;
}

function performLessonSearch(index, query) {
  const queryTokens = tokenizeSearch(query);
  if (!queryTokens.length) return [];
  const expanded = expandSearchTokens(queryTokens);

  return index
    .map((section) => {
      const scoring = scoreSearchSection(section, query, queryTokens, expanded);
      return {
        section,
        score: typeof scoring === "number" ? scoring : scoring.score,
        freq: typeof scoring === "number" ? 0 : scoring.freq.total,
        queryHits: typeof scoring === "number" ? 0 : scoring.freq.queryHits
      };
    })
    .filter((item) => item.score > 0 || item.freq > 0)
    .sort((a, b) => {
      const freqDiff = b.freq - a.freq;
      if (Math.abs(freqDiff) > SEARCH_FREQ_TIE_TOLERANCE) return freqDiff;

      // При близкой частоте отдаём приоритет тому, что раньше в программе.
      const sourceDiff = (a.section.sourceOrder || 0) - (b.section.sourceOrder || 0);
      if (sourceDiff !== 0) return sourceDiff;
      const sectionDiff = (a.section.sectionOrder || 0) - (b.section.sectionOrder || 0);
      if (sectionDiff !== 0) return sectionDiff;

      return b.score - a.score;
    })
    .slice(0, 12)
    .map((item) => ({
      ...item.section,
      score: item.score,
      freq: item.freq,
      snippet: extractSearchSnippet(item.section, queryTokens)
    }));
}

function formatSearchPathTail(sectionPath) {
  const parts = String(sectionPath || "")
    .split("→")
    .map((part) => part.trim())
    .filter(Boolean);
  if (parts.length <= 2) return parts.join(" → ");
  return `${parts[parts.length - 2]} → ${parts[parts.length - 1]}`;
}

